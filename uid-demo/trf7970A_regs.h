#ifndef TRF7970A_REGS_REGS_H
#define TRF7970A_REGS_REGS_H

#include <bsp/utility.h>

typedef struct {
	uint32_t reserved_00[3];
	uint8_t irq_status;
#define TRF7970A_REGS_IRQ_STATUS_NORESP BSP_BIT8(0)
#define TRF7970A_REGS_IRQ_STATUS_COL BSP_BIT8(1)
#define TRF7970A_REGS_IRQ_STATUS_ERR3 BSP_BIT8(2)
#define TRF7970A_REGS_IRQ_STATUS_ERR2 BSP_BIT8(3)
#define TRF7970A_REGS_IRQ_STATUS_ERR1 BSP_BIT8(4)
#define TRF7970A_REGS_IRQ_STATUS_FIFO BSP_BIT8(5)
#define TRF7970A_REGS_IRQ_STATUS_SRX BSP_BIT8(6)
#define TRF7970A_REGS_IRQ_STATUS_TX BSP_BIT8(7)
	uint8_t reserved_0d[15];
	uint8_t fifo_status;
#define TRF7970A_REGS_FIFO_STATUS_BYTES(val) BSP_FLD8(val, 0, 6)
#define TRF7970A_REGS_FIFO_STATUS_BYTES_GET(reg) BSP_FLD8GET(reg, 0, 6)
#define TRF7970A_REGS_FIFO_STATUS_BYTES_SET(reg, val) BSP_FLD8SET(reg, val, 0, 6)
#define TRF7970A_REGS_FIFO_STATUS_OVERFLOW BSP_BIT8(7)
} trf7970a_regs;

#endif /* TRF7970A_REGS_REGS_H */
